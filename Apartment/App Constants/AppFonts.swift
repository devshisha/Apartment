        static var system16 = UIFont.systemFont(ofSize: 16)

        static var bold18 = UIFont.systemFont(ofSize: 18, weight: .bold)
        static var bold20 = UIFont.systemFont(ofSize: 20, weight: .bold)

        static var semiBold16 = UIFont.systemFont(ofSize: 16, weight: .semibold)

    }
}
